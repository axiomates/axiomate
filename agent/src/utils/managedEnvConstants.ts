/**
 * Environment variables that control inference routing.
 *
 * When AXIOMATE_CODE_PROVIDER_MANAGED_BY_HOST is truthy in the spawn env,
 * these are stripped from settings-sourced env so the host's routing config
 * isn't overridden by a user's ~/.axiomate/settings.json.
 */
const PROVIDER_MANAGED_ENV_VARS = new Set([
  'AXIOMATE_CODE_PROVIDER_MANAGED_BY_HOST',
  'AXIOMATE_BASE_URL',
  'AXIOMATE_API_KEY',
  'AXIOMATE_AUTH_TOKEN',
  'AXIOMATE_MODEL',
  'AXIOMATE_CUSTOM_HEADERS',
  'AXIOMATE_CODE_SUBAGENT_MODEL',
])

export function isProviderManagedEnvVar(key: string): boolean {
  return PROVIDER_MANAGED_ENV_VARS.has(key.toUpperCase())
}

